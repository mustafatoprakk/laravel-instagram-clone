    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $posts = new Post();
        $posts->user_id = $request->user_id;
        $posts->user_name = Auth::user()->name;
        $posts->title = $request->title;
        $posts->description = $request->description;

        if ($request->hasFile("post_image")) {
            $post_image = $request->file("post_image");
            $extention = $post_image->getClientOriginalExtension();
            $imageName = time() . "." . $extention;
            $post_image->move("uploads/posts/", $imageName);
            $posts->post_image = $imageName;
        }
        $posts->save();

        return redirect()->back()->with("message", "Post has been created successfully!");
    }

